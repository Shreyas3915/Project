        <p>Dr. Babasaheb Ambedkar Technological University was established by the Government of Maharashtra vide Dr. Babasaheb Ambedkar Technological University Act. No. XXII of 1989 as a Unitary University. Later, the Government of Maharashtra converted this University to the Affiliating Technological University since 2016 vide Dr. Babasaheb Ambedkar Technological University Act No. XXIX of 2014 with the jurisdiction of the University as the entire state of Maharashtra. Presently 272 institutes are affiliated with the University and the total student strength is 1,36,534.
          The main thrust of the University is to develop and provide state-of-art infrastructure and an excellent academic and research environment to its faculty and students. The university has kept pace with the times by introducing the CBCS system, and multidisciplinary and interdisciplinary programs - courses. It has an efficient and transparent governance system, support for researchers, clean and green campus, gender friendly atmosphere, ample opportunity for students to hone their skills in Co-curricular activities, technologically equipped classrooms and laboratories, a state-of-the-art library, an incubation center titled, “DBATU Forum of Innovation, Incubation & Enterprise” (Section-8 Company) with support from Maharashtra State Innovation Society, a nodal agency of Govt. of Maharashtra, wellequipped health center with practicing medical professionals, playgrounds. Our university has already started implementing provisions of the NEP 2020.</p>
